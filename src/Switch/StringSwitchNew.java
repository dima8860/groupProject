package Switch;

public class StringSwitchNew {
    public static void main(String[] args) {
        String fruit = "apple";

        switch (fruit) {
            case "apple":
                System.out.println(" This is an apple");
                break;
            case "banana":
                System.out.println(" This is a banana");
                break;
            default:
                System.out.println("We are out of them");
        }


    }
}



