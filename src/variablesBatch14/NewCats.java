package variablesBatch14;

public class NewCats {

    public static void main(String[] args) {

        int wild = 20;
        int pets = 0;

        System.out.println("Wild: " + wild);
        System.out.println("Pets: " + pets);
        System.out.println();

        wild -= 5;
        pets += 5;

        System.out.println("Wild: " + wild);
        System.out.println("Pets: " + pets);
        System.out.println();

        wild -= 5;
        pets += 5;

        System.out.println("Wild: " + wild);
        System.out.println("Pets: " + pets);
        System.out.println();
    }
}
