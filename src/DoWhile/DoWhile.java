package DoWhile;



public class DoWhile {
    public static void main(String[] args) {
      /* Do While execute the statement at least one time and then it will check While loop;
        Syntax
         do {
         statement;
         } while (boolean);


         flow

       */
      int num = 20;

      do {
          System.out.println(num);
          num++;
      } while (num < 10);
        System.out.println(num);
    }

}
